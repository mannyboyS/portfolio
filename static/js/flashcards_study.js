let currentIndex = 0;
let cardOrder = [];
let isFlipped = false;

// Initialize card order randomly
function initializeCards() {
    cardOrder = Array.from({ length: flashcardsData.length }, (_, i) => i);
    shuffleArray(cardOrder);
    displayCard();
    updateCounter();
    updateProgress();
}

// Shuffle array using Fisher-Yates algorithm
function shuffleArray(array) {
    for (let i = array.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [array[i], array[j]] = [array[j], array[i]];
    }
}

// Display current card
function displayCard() {
    const cardIndex = cardOrder[currentIndex];
    const card = flashcardsData[cardIndex];

    document.getElementById('question-text').textContent = card.question;

    const choicesList = document.getElementById('choices-list');
    choicesList.innerHTML = card.choices.map(choice => `<li>${choice}</li>`).join('');

    document.getElementById('answer-text').textContent = card.answer;

    // Reset flip state
    const flashcard = document.getElementById('flashcard');
    flashcard.classList.remove('flipped');
    isFlipped = false;

    // Update button states
    updateButtons();
}

// Flip card
function flipCard() {
    const flashcard = document.getElementById('flashcard');
    flashcard.classList.toggle('flipped');
    isFlipped = !isFlipped;
}

// Next card
function nextCard() {
    if (currentIndex < cardOrder.length - 1) {
        currentIndex++;
        displayCard();
        updateCounter();
        updateProgress();
    }
}

// Previous card
function previousCard() {
    if (currentIndex > 0) {
        currentIndex--;
        displayCard();
        updateCounter();
        updateProgress();
    }
}

// Shuffle cards
function shuffleCards() {
    shuffleArray(cardOrder);
    currentIndex = 0;
    displayCard();
    updateCounter();
    updateProgress();
}

// Update counter
function updateCounter() {
    document.getElementById('current-card').textContent = currentIndex + 1;
    document.getElementById('total-cards').textContent = flashcardsData.length;
}

// Update progress bar
function updateProgress() {
    const progress = ((currentIndex + 1) / flashcardsData.length) * 100;
    document.getElementById('progress-fill').style.width = progress + '%';
}

// Update button states
function updateButtons() {
    const prevBtn = document.getElementById('prev-btn');
    const nextBtn = document.getElementById('next-btn');

    prevBtn.disabled = currentIndex === 0;
    nextBtn.disabled = currentIndex === cardOrder.length - 1;
}

// Keyboard navigation
document.addEventListener('keydown', (e) => {
    if (e.key === 'ArrowLeft') {
        previousCard();
    } else if (e.key === 'ArrowRight') {
        nextCard();
    } else if (e.key === ' ' || e.key === 'Enter') {
        e.preventDefault();
        flipCard();
    }
});

// Initialize on page load
initializeCards();